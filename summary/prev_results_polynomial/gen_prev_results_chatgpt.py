def generate_rows() -> list[dict[str, object]]:
    rows: list[dict[str, object]] = []
    for problem in ("full", "cyclic", "extfield"):
        for p, m, q in FIELD_PARAMS:
            entries = TABLE_BY_PROBLEM_Q[problem][q]
            if len(entries) != 11:
                raise ValueError(f"Expected 11 degrees for {problem=}, {q=}")
            for n, (lb, lb_ref_key, ub, ub_ref_key) in enumerate(entries, start=1):
                if lb > ub:
                    raise ValueError(f"Inconsistent bounds for {problem=}, {q=}, {n=}: {lb} > {ub}")
                rows.append({
                    "problem": problem,
                    "p": p,
                    "m": m,
                    "q": q,
                    "n": n,
                    "lb": lb,
                    "lb_ref": REFERENCE_REGISTRY[lb_ref_key],
                    "ub": ub,
                    "ub_ref": REFERENCE_REGISTRY[ub_ref_key],
                })
    if len(rows) != 330:
        raise ValueError(f"Expected 330 rows, found {len(rows)}")
    return rows


def write_csv(output: Path) -> None:
    rows = generate_rows()
    output.parent.mkdir(parents=True, exist_ok=True)
    with output.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=COLUMNS, lineterminator="\n")
        writer.writeheader()
        writer.writerows(rows)


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--output",
        type=Path,
        default=Path("prev_results.csv"),
        help="CSV output path; default: ./prev_results.csv",
    )
    args = parser.parse_args()
    write_csv(args.output)
    print(f"Wrote {args.output} with 330 rows.")


if __name__ == "__main__":
    main()
